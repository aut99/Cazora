function opennav() {
  document.querySelector(".bmenu").style.display = "none";
  document.querySelector(".mobilnav").style.display = "block";
  document.querySelector(".xmenu").style.display = "block";
}

function closenav() {
    document.querySelector(".bmenu").style.display = "block";
    document.querySelector(".mobilnav").style.display = "none";
    document.querySelector(".xmenu").style.display = "none";
}


